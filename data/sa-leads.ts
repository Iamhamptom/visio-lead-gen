/** Get leads by category */
export function getLeadsByCategory(category: string): MarketplaceLead[] {
    if (category === 'All') return SA_MUSIC_LEADS;
    return SA_MUSIC_LEADS.filter(lead =>
        lead.category.toLowerCase().includes(category.toLowerCase()) ||
        lead.tags?.some(tag => tag.toLowerCase().includes(category.toLowerCase()))
    );
}

/** Search leads by name or handle */
